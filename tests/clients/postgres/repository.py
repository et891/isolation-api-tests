from contextlib import contextmanager
from typing import Generator, TypeVar

from sqlalchemy.orm import sessionmaker, Session

from tests.clients.postgres.base import BaseTestModel

# Тип-переменная для ORM-моделей тестового слоя.
#
# Ограничиваем её BaseTestModel, чтобы:
# - repository работал только с тестовыми моделями;
# - случайно не передать сюда произвольный объект;
# - сохранить типизацию методов на уровне тестового кода.
ModelT = TypeVar("ModelT", bound=BaseTestModel)


class PostgresTestRepository:
    """
    Базовый Postgres repository тестового слоя.

    Это инфраструктурный компонент, который:
    - управляет жизненным циклом SQLAlchemy Session;
    - инкапсулирует транзакционную модель;
    - предоставляет минимальный набор операций записи.

    Важный архитектурный момент:
    repository в тестовом слое — это не бизнес-репозиторий.

    Он:
    - не знает бизнес-смысл моделей;
    - не реализует сложные запросы;
    - не содержит ассертов и проверок.

    Его задача — дать тестам
    безопасный, предсказуемый и контролируемый
    доступ к базе данных.
    """

    def __init__(self, session_factory: sessionmaker[Session]) -> None:
        # session_factory передаётся извне, чтобы:
        # - repository не создавал engine самостоятельно;
        # - не зависел от окружения и конфигурации;
        # - мог использоваться в разных тестовых сценариях.
        #
        # Это полностью повторяет подход,
        # который мы уже использовали для HTTP и Kafka клиентов.
        self.session_factory = session_factory

    @contextmanager
    def session_read(self) -> Generator[Session, None, None]:
        """
        Контекстный менеджер для операций чтения.

        Используется в тестах, где:
        - требуется только читать данные;
        - не планируется модификация состояния БД;
        - не нужна явная транзакционная логика.

        Мы сознательно не используем здесь commit/rollback:
        - сессия живёт ровно в рамках контекста;
        - после выхода она всегда закрывается;
        - тест не оставляет за собой открытых соединений.
        """
        session = self.session_factory()
        try:
            yield session
        finally:
            # Закрытие сессии гарантировано
            # независимо от того, упал тест или нет.
            session.close()

    @contextmanager
    def session_write(self) -> Generator[Session, None, None]:
        """
        Контекстный менеджер для операций записи.

        Это точка, где зафиксирована транзакционная модель тестов.

        Архитектурный смысл:
        - каждая операция записи выполняется в транзакции;
        - commit происходит только если код внутри
          контекста выполнился без ошибок;
        - при любом исключении выполняется rollback.

        Такой подход:
        - защищает БД от частично записанных данных;
        - делает тесты воспроизводимыми;
        - упрощает отладку при падениях.
        """
        session = self.session_factory()
        try:
            yield session
            # Явный commit фиксирует изменения в БД
            # только после успешного выполнения логики.
            session.commit()
        except Exception:
            # При любой ошибке состояние БД откатывается.
            #
            # Это критично для тестов, которые:
            # - падают посередине сценария;
            # - не должны оставлять "грязные" данные.
            session.rollback()
            raise
        finally:
            # Сессия закрывается в любом случае,
            # чтобы не протекали соединения.
            session.close()

    def create(self, instance: ModelT) -> ModelT:
        """
        Сохранение ORM-модели в базе данных.

        Используется в тестах для:
        - подготовки данных перед вызовом API;
        - настройки начального состояния системы;
        - сценариев проверки фильтрации, сортировки и выборок.

        Repository здесь:
        - не валидирует модель;
        - не проверяет бизнес-инварианты;
        - предполагает, что данные уже корректны
          с точки зрения тестового сценария.
        """
        with self.session_write() as session:
            # Добавляем объект в текущую сессию.
            session.add(instance)

            # flush отправляет изменения в БД,
            # но не завершает транзакцию.
            #
            # Это позволяет:
            # - получить сгенерированные поля (id, timestamps);
            # - продолжать работу в рамках одной транзакции.
            session.flush()

            # refresh обновляет объект из БД,
            # гарантируя, что в instance
            # лежит актуальное состояние.
            session.refresh(instance)

        return instance
